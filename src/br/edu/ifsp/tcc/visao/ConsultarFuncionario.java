    public void atualizaTabela() {
        DefaultTableModel dtm = (DefaultTableModel) jtbPesqFuncionario.getModel(); // manipulador de tabela DefaultTableModel
        dtm.setNumRows(0);//apagando todas as linhas / o zero serva para apagar as linhas "√© muito rapido"
        for (Funcionario f : listaFuncionarios) {
            //String.format("%.2f",f.getPrecoUnitario()) -> mostra com duas casa apos a virgula
            dtm.addRow(new Object[]{f.getNome(), f.getRg()}); // Object √© um vetor ou cole√ß√£o com ele n√£o pecisa converter
        }
    }

