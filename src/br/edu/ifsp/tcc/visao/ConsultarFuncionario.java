    public void pesquisar() {
        String str = "from Funcionario c ";
        String atributo = "";
        if (jcbPesquisarFuncionario.getSelectedItem().toString().equals("Nome")) {
            atributo = "c.nome";
        }
        if (jcbPesquisarFuncionario.getSelectedItem().toString().equals("R.G")) {
            atributo = "c.rg";
        }
        txtConsultarFuncionario.requestFocus();
        String filtro = txtConsultarFuncionario.getText();
        if (filtro.length() > 0 && filtro != null) {
            str += "where upper(" + atributo + ") like '%" + filtro.toUpperCase() + "%' ";
        }
        str += "order by " + atributo;
        listaFuncionarios.clear();//limpa lista
        listaFuncionarios.addAll(controle.pesquisarFuncionarios(str));//ATUALIZA A LISTA COM OS DADOS DESTA PESQUISA RELEASE
        DefaultTableModel dtm = (DefaultTableModel) jtbPesqFuncionario.getModel();
        dtm.setNumRows(0);//LIMPA A TABELA
        atualizaTabela();//PREENCHE COM OS NOVOS DADOS
    }

