    public void salvar() throws ParseException {

        f.setNome(txtNome.getText());
        f.setSigla(txtSigla.getText());
        f.setCidade(txtCidade.getText());
        f.setRegime(String.valueOf(jComboBoxRegime.getSelectedItem()));
        f.setCordenadoria((Coordenadoria) jComboBoxSiglaCoordenadoria.getSelectedItem()); // foi feito uma conversão para String?
        controle.salvar(f);
        limparCampos();
        carregarLista();
        atualizaTabela();
    }
    
    public void vincularCampos() {
        Presidio f = presidios.get(jTablePresidio.getSelectedRow());// pega a linha da tabela
        txtNome.setText(f.getNome());
        txtSigla.setText(f.getSigla());
        txtCidade.setText(f.getCidade());
        jComboBoxSiglaCoordenadoria.setSelectedItem(f.getSigla()); // pega a bebida do comboBox
        jComboBoxRegime.setSelectedItem(f.getRegime()); // pega a bebida do comboBox
   
    }
    
