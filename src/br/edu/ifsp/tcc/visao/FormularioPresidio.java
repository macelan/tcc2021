    public boolean validaCampos() {
        if (!(txtNome.getText().length() > 0)) {
            JOptionPane.showMessageDialog(null, "Informe o Nome do Presídio.");
            return false;
        }
        return true;
    }
    
    public void limparCampos() {
        txtNome.setText("");
        txtSigla.setText("");
        txtCidade.setText("");
        jComboBoxRegime.addItem("");
        txtLocalizar.setText("");
        jComboBoxRegime.setSelectedIndex(0);
        jComboBoxSiglaCoordenadoria.setSelectedIndex(0);
              
        
    }
    
    public void carregarLista(){
        presidios = controle.listarTodos();
        
    }
    
    public void pesquisar(){
        String str = "from Presidio f ";
        String atributo = "f.nome";

        String filtro = txtLocalizar.getText();
        
        if (filtro.length() > 0 && filtro != null) {
            str += "where upper(" + atributo + ") like '%" + filtro.toUpperCase() + "%' ";
        }

        str += "order by " + atributo;
        pesquisarDinamico(str);//ATUALIZA A LISTA COM OS DADOS DESTA PESQUISA DINAMICA
        atualizaTabela();//PREENCHE COM OS NOVOS DADOS
    }
    
    public void pesquisarDinamico(String comando) {
        this.presidios = controle.pesquisarPresidios(comando);
    }
