    public void atualizaTabela() {
        DefaultTableModel dtm = (DefaultTableModel) jtbPesqSentenciado.getModel(); // manipulador de tabela DefaultTableModel
        dtm.setNumRows(0);//apagando todas as linhas / o zero serva para apagar as linhas "é muito rapido"
        for (Sentenciado f : listaSentenciados) {
            dtm.addRow(new Object[]{f.getMatricula(), f.getNome(), f.getRg()}); // Object é um vetor ou coleção com ele não pecisa converter
        }
    }

