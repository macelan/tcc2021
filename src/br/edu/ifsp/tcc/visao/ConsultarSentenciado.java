    public void pesquisar() {
        String str = "from Sentenciado c ";
        String atributo = "";
        if (jcbPesquisarSentenciado.getSelectedItem().toString().equals("Nome")) {
            atributo = "c.nome";
        }
        if (jcbPesquisarSentenciado.getSelectedItem().toString().equals("Matricula")) {
            atributo = "c.matricula";
        }
        if (jcbPesquisarSentenciado.getSelectedItem().toString().equals("R.G")) {
            atributo = "c.rg";
        }
        txtConsultarSentenciado.requestFocus();
        String filtro = txtConsultarSentenciado.getText();
        if (filtro.length() > 0 && filtro != null) {
            str += "where upper(" + atributo + ") like '%" + filtro.toUpperCase() + "%' ";
        }
        str += "order by " + atributo;
        listaSentenciados.clear();//limpa lista
        listaSentenciados.addAll(controle.pesquisarSentenciados(str));//ATUALIZA A LISTA COM OS DADOS DESTA PESQUISA RELEASE
        DefaultTableModel dtm = (DefaultTableModel) jtbPesqSentenciado.getModel();
        dtm.setNumRows(0);//LIMPA A TABELA
        atualizaTabela();//PREENCHE COM OS NOVOS DADOS
    }

