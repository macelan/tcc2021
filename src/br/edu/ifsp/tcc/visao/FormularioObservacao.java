     public void salvar() throws ParseException {
        f.setDataOcorrido(df.parse(dateObservacao.getText()));//passando data do formato string para Date
        f.setRelato(txtAreaObservacao.getText());
        f.setDataRegistro(new Date ());
        f.setSentenciados((Sentenciado)jComboBoxSentenciado.getSelectedItem()); // foi feito uma conversão para String?
        controle.salvar(f);
        limparCampos();
        carregarLista();
        atualizaTabela();
     } 
        
        public void vincularCampos() {
        Observacao f = observacoes.get(jTableObservacao.getSelectedRow());// pega a linha da tabela
        dateObservacao.setText(df.format(f.getDataOcorrido()));
        txtAreaObservacao.setText(f.getRelato());
        }
   
