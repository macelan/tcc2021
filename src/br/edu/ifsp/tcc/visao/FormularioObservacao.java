    public void atualizaTabela() {
        DefaultTableModel dtm = (DefaultTableModel) jTableObservacao.getModel();
        dtm.setNumRows(0);//apagando todas as linhas
        for (Observacao f : observacoes)
            {dtm.addRow(new Object[]
            {dfh.format(f.getDataRegistro()), 
              df.format(f.getDataOcorrido()), 
                        f.getRelato()});
        }

    }
