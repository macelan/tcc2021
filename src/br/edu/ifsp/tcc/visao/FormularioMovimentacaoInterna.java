     public void salvar() throws ParseException {
        f.setDataMovimentacao(df.parse(dataMovimentacaoInterna.getText()));//passando data do formato string para Date
        f.setDataOcorrido(new Date ());
        f.setTipo((String) jComboBoxTipo.getSelectedItem()); // foi feito uma conversão para String
        controle.salvar(f);
        limparCampos();
        carregarLista();
        atualizaTabela();
     } 
        
