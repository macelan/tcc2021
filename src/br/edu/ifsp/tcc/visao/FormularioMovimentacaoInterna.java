  public boolean validaCampos() {
        if (!(dataMovimentacaoInterna.getText().length() > 0)) {
            JOptionPane.showMessageDialog(null, "Informe o Data da Movimentação.");
            return false;
        }
        return true;
    }
  
   public void carregarLista(){
        movimentacoesInternas = controle.listarTodos();
   }
   
   public void pesquisar(){
        String str = "from MovimentacaoInterna f ";
        String atributo = "f.tipo";

        String filtro = txtLocalizar.getText();
        
        if (filtro.length() > 0 && filtro != null) {
            str += "where upper(" + atributo + ") like '" + filtro.toUpperCase() + "%' ";
        }

        str += "order by " + atributo;
        pesquisarDinamico(str);//ATUALIZA A LISTA COM OS DADOS DESTA PESQUISA DINAMICA
        atualizaTabela();//PREENCHE COM OS NOVOS DADOS
    }
   
   public void pesquisarDinamico(String comando) {
        this.movimentacoesInternas = controle.pesquisarMovimentacoes(comando);
    }
   
